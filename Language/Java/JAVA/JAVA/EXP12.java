// STRINGS
public class EXP12{
    public static void main(String args[]){
        String a="YUVRAJ HINGER";
        String b="HELLO WORLD";
        System.out.println("FOR CHAR AT 1 LOC IN STRING "+a.charAt(1));
        System.out.println("FOR LOWER CASE IN STRING "+a.toLowerCase());
        System.out.println("FOR UPPER CASE IN STRING "+a.toUpperCase());
        System.out.println("CHECK WEATHER STRING END WITH GERS "+a.endsWith("GERS"));
        System.out.println("FOR CONCAT TO STRING "+a.concat(b));
        System.out.println("COMPARE TO b "+a.compareTo(a));
    }
}